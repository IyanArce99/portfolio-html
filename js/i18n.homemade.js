function setContentMessage(id) {
    let element = document.getElementById(id);
    element.appendChild(document.createTextNode(i18nHomemade[id][language]));
}



// load all messages
window.onload = function () {
    arrayKeys.forEach((key)=> {
        setContentMessage(key);
    });
};

